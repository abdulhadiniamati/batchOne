package com.mattic.collectionexamples;

public class Phonebook {

	
	 	private String name ;
	 	private int phone;
	 	private String add;
	 	
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getPhone() {
			return phone;
		}
		public void setPhone(int phone) {
			this.phone = phone;
		}
		public String getAdd() {
			return add;
		}
		public void setAdd(String add) {
			this.add = add;
		}
	 	
	 	
	
	
	
}
